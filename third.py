#-----------------operators--------------#

x = 6
y = 4

#Arithmetic operator

Addition = x + y
Subtraction = x - y
Multiplication = x * y
Division = x / y
Modulus = x % y
Exponentiation = x ** y
Floor_division = x // y
print('Addition is : ', Addition)
print('Subtraction is : ', Subtraction)
print('Multiplication is : ', Multiplication)
print('Division is : ', Division)
print('Modulus is : ', Modulus)
print('Exponentiation is : ', Exponentiation)
